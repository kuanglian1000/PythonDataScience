import numpy as np

# Use a list to create a NumPy array.
arr = np.array([1,2,3,4,5]) #create ndarray by array() function

print(arr)
print(type(arr))

# Use a tuple to create a NumPy array.
arr = np.array((1,3,4,5,6))
print(arr)

#=== Dimensions in Arrays ===
# 0-D arrays
arr = np.array(42)
print(arr)
